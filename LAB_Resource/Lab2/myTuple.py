#my tuple created
myTuple = ("pop", "rock", "soul", "hard work", "soft wark", "R&B", "progressive rock", "disco")

#print my tuple
print("My Tuple: ", myTuple) 

#print tuple length
print("Length: ", len(myTuple)) 

#access element 
print("tuple[3]: ", myTuple[3])

#slice first two value of tuple
print("First two value: ", myTuple[0:2])

#slice specific value of tuple
print("2 to 5 value: ", myTuple[2:5])

#specific value index
print("Index: ", myTuple.index("hard work")) 